/*:
 ## let
 
 `let` indica que la variable es inmutable (constante).
 
 Solo puede tener un valor, una vez asignado este no puede cambiar
 
 */
let birthday = "26/03/1988"
/*:
 ## var
 
 `var` indica que la variable es mutable, nuevos valores pueden ser asignados.
 
 Recordar. Esos valores tienen que ser del mismo tipo *(ver Types..)*
 
 */
var age = 28

